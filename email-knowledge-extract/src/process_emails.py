#!/usr/bin/env python3
"""Process the local email corpus -> knowledge base + contacts DB.

Reads emails from the local archive DB (the `emails` table) — NO live mail calls
here; a separate downloader is expected to populate that table. For each unprocessed
record:
  - record/merge every contact (headers + body, enriched by the model)
  - extract product knowledge (our outbound mail) -> product-knowledge.md
  - extract operational knowledge (any mail) -> operational-knowledge.md
  - optionally ingest attachments via an external doc pipeline (if configured)
  - append a dated activity line per contact; refresh activity summaries
Idempotent: processed message IDs are tracked.
"""
import os, sys, json, time, datetime, subprocess, re
from email.utils import getaddresses

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
import db as DB
import extract as EX
try:
    import qa_kb as QA   # OPTIONAL Q&A-harvest pass (ships with the semantic-index skill).
except Exception:        # Not included here — the pipeline runs fine without it.
    QA = None
from kbconf import (INTERNAL_ADDRESSES, OWN_DOMAIN, PRODUCT_KB, OPERATIONAL_KB,
                    DOCPIPE, is_noise_sender, is_internal_chat, is_forward, parse_forwarded,
                    DAY_MAX, NIGHT_MAX, DAY_BUDGET, NIGHT_BUDGET, RECENT_DAYS,
                    MAX_EXTRACT_RETRIES, WRITERS, WRITING_STYLES_DIR)


def _refresh_style_profile(person, observations):
    """Merge this run's observations into the bounded learned-<person>.md (no growing logs)."""
    obs = list(dict.fromkeys(o.strip() for o in observations if o.strip()))[:40]  # dedupe + cap
    if not obs:
        return
    os.makedirs(WRITING_STYLES_DIR, exist_ok=True)
    path = os.path.join(WRITING_STYLES_DIR, f"learned-{person}.md")
    existing = ""
    if os.path.exists(path):
        existing = "\n".join(l for l in open(path).read().splitlines()
                             if not l.startswith("# ") and not l.startswith("_Auto"))
    prof = EX.update_style_profile(person.capitalize(), existing.strip(), obs)
    if prof:
        with open(path, "w") as f:
            f.write(f"# Learned writing style — {person.capitalize()} (from sent emails)\n\n"
                    f"_Auto-generated by the email cron from {person.capitalize()}'s sent mail; "
                    f"complements any manual profile. Kept concise (merged, not appended)._\n\n{prof}\n")
        print(f"{_now()} refreshed learned style profile -> learned-{person}.md")


def _now():
    return datetime.datetime.now().isoformat(timespec="seconds")


def _addrs(value):
    return [a.lower() for _, a in getaddresses([value or ""]) if a and "@" in a]


_EMAIL_RE = re.compile(r"[A-Za-z0-9._%+\-]+@[A-Za-z0-9.\-]+\.[A-Za-z]{2,}")


def deterministic_contacts(frm, to, cc, body):
    """Ground-truth contact roster built with NO LLM — addresses are parseable tokens, so
    the model never gets to decide which contacts exist (only to enrich them).

    Returns {addr: display_name}. Header addresses (From/To/Cc) are authoritative and carry
    their display name for free via getaddresses. Body-embedded addresses (forwarded/quoted
    mail) are caught by regex so a real sender hiding in a forward isn't lost — filtered
    through the same noise/internal rules and marked with an empty name (headers win on name).
    """
    ground = {}
    for hdr in (frm, to, cc):                       # authoritative: real recipients/senders
        for disp, addr in getaddresses([hdr or ""]):
            addr = addr.lower().strip()
            if addr and "@" in addr:
                ground.setdefault(addr, (disp or "").strip())
    for m in _EMAIL_RE.findall(body or ""):         # forwarded/quoted addresses in the body
        addr = m.lower().strip(".")
        if addr in ground:
            continue
        if addr in INTERNAL_ADDRESSES or is_noise_sender(addr):
            continue                                # drop no-reply/mailer-daemon/marketing footers
        ground.setdefault(addr, "")
    return ground


def _append_kb(path, header, items):
    if not items:
        return
    os.makedirs(os.path.dirname(path), exist_ok=True)
    new = not os.path.exists(path)
    with open(path, "a") as f:
        if new:
            title = "Product" if "product" in os.path.basename(path) else "Operational"
            f.write(f"# {title} Knowledge — extracted from email\n\n"
                    f"_Auto-generated by the email-KB cron. Each entry cites its source email._\n\n")
        f.write(f"### {header}\n")
        for it in items:
            f.write(f"- {it}\n")
        f.write("\n")


def _ingest_attachments(paths):
    if not DOCPIPE:                                 # attachment ingest disabled
        return []
    done = []
    for p in paths or []:
        if not os.path.exists(p):
            continue
        try:
            r = subprocess.run([DOCPIPE, "ingest", p], capture_output=True, text=True, timeout=900)
            done.append((os.path.basename(p), "indexed" if r.returncode == 0 else f"err{r.returncode}"))
        except Exception as e:
            done.append((os.path.basename(p), f"err:{e}"))
    return done


def _process_forward(rec, c, touched, counters, when, mid):
    """Parse the original message inside a forwarded email and extract it as inbound.

    Recovers the original sender (often an end customer) and their inquiry, which the
    outer message hides because its From: is one of us. Records the original sender as
    a contact and files their operational facts / activity under the real inbound mail.
    """
    fwd = parse_forwarded(rec.get("subject", ""), rec.get("body", ""))
    if not fwd:
        return
    orig_from = (_addrs(fwd["orig_from"]) or [""])[0]
    if not orig_from or is_noise_sender(orig_from):
        return
    # Skip if the forwarded original is ALSO from us (internal fwd of our own mail) —
    # nothing new to learn about an external party.
    if orig_from.endswith("@" + OWN_DOMAIN):
        return
    fdir = "inbound (forwarded internally -> us)"
    fsubj = fwd.get("orig_subject") or rec.get("subject", "")
    data = EX.extract(fdir, fwd["orig_from"], fwd.get("orig_to", ""), fwd.get("orig_cc", ""),
                      fsubj, fwd.get("orig_body", ""))
    details = {(ct.get("email") or "").lower(): ct for ct in data.get("contacts", []) if ct.get("email")}
    act = data.get("activity", "") or f"Email: {fsubj}"
    # Same discipline as the main path: Python owns the roster (forwarded original's
    # headers + body), the LLM only enriches. Reconcile the model against it.
    ground = deterministic_contacts(fwd["orig_from"], fwd.get("orig_to", ""),
                                    fwd.get("orig_cc", ""), fwd.get("orig_body", ""))
    for a in details:
        if a and a not in ground and a not in INTERNAL_ADDRESSES:
            counters["halluc"] = counters.get("halluc", 0) + 1
            print(f"    ⚠ hallucinated contact (forwarded, not in headers/body): {a} — dropped")
    for addr, hdr_name in ground.items():
        if addr in INTERNAL_ADDRESSES or is_noise_sender(addr):
            continue
        d = details.get(addr, {})
        if not d:
            counters["miss"] = counters.get("miss", 0) + 1
        DB.upsert_contact(c, addr, when, name=d.get("name") or hdr_name, company=d.get("company", ""),
                          role=d.get("role", ""), phone=d.get("phone", ""),
                          activity_line=f"(forwarded internally) {act}")
        touched.add(addr)
    if data.get("operational_knowledge"):
        prov = (f"{when[:10]} · {fdir} · Orig-From: {orig_from} · "
                f"Subject: {fsubj or '(no subject)'} · [{mid}]")
        _append_kb(OPERATIONAL_KB, prov, data["operational_knowledge"])
        counters["ops"] += len(data["operational_knowledge"])
    counters["fwd"] = counters.get("fwd", 0) + 1
    print(f"    forwarded original: {orig_from} | {fsubj[:40]}")


def _process_record(rec, c, touched, counters, style_seen):
    mid = rec["id"]
    frm, to, cc = rec.get("from", ""), rec.get("to", ""), rec.get("cc", "")
    subj, body = rec.get("subject", ""), rec.get("body", "")
    body_new = rec.get("body_new") or body   # quoted reply chain stripped; full body kept for fwd parsing
    when = _now()
    from_addr = (_addrs(frm) or [""])[0]
    # Skip our own internal agent/assistant chats (any mail involving an agent account):
    # archived, but never extracted into the CRM/KB.
    if is_internal_chat(frm, to, cc):
        DB.mark_processed(c, mid, when); c.commit()
        print(f"  [{rec.get('account','?')}] skipped (internal agent chat) | {subj[:40]}")
        counters["internal"] = counters.get("internal", 0) + 1
        return
    # Skip marketing/social/automated senders: keep in the archive, no CRM extraction.
    if is_noise_sender(from_addr):
        DB.mark_processed(c, mid, when); c.commit()
        print(f"  [{rec.get('account','?')}] skipped (noise sender: {from_addr}) | {subj[:40]}")
        counters["noise"] = counters.get("noise", 0) + 1
        return
    outbound = from_addr.endswith("@" + OWN_DOMAIN)
    direction = "outbound (us -> client)" if outbound else "inbound (-> us)"

    try:
        data = EX.extract(direction, frm, to, cc, subj, body_new)
    except EX.LLMTimeout:
        # Extraction call timed out. DEFER: leave the email unprocessed so a later
        # run retries it — but cap retries so a chronically-slow email can't block
        # the oldest-first queue forever. After the cap, give up and move on.
        n = DB.bump_extract_attempt(c, mid, when); c.commit()
        if n >= MAX_EXTRACT_RETRIES:
            DB.mark_processed(c, mid, when); c.commit()
            counters["timeout_gaveup"] = counters.get("timeout_gaveup", 0) + 1
            print(f"  [{rec.get('account','?')}] extraction timed out {n}x — GAVE UP, marking processed | {subj[:40]}")
        else:
            counters["timeout_deferred"] = counters.get("timeout_deferred", 0) + 1
            print(f"  [{rec.get('account','?')}] extraction timed out (attempt {n}/{MAX_EXTRACT_RETRIES}) — deferred for retry | {subj[:40]}")
        return
    details = {(ct.get("email") or "").lower(): ct for ct in data.get("contacts", []) if ct.get("email")}

    # Deterministic roster first — Python decides WHICH contacts exist; the LLM only enriches.
    ground = deterministic_contacts(frm, to, cc, body_new)

    # Reconcile the model against ground truth: any address the LLM emitted that appears in
    # NO header and NO body is fabricated — log it, never store it.
    for a in details:
        if a and a not in ground and a not in INTERNAL_ADDRESSES:
            counters["halluc"] = counters.get("halluc", 0) + 1
            print(f"    ⚠ hallucinated contact (not in headers/body): {a} — dropped")

    for addr, hdr_name in ground.items():
        if addr in INTERNAL_ADDRESSES:
            continue
        d = details.get(addr, {})
        if not d:
            counters["miss"] = counters.get("miss", 0) + 1   # real address the LLM overlooked
        DB.upsert_contact(c, addr, when,
                          name=d.get("name") or hdr_name, company=d.get("company", ""),
                          role=d.get("role", ""), phone=d.get("phone", ""),
                          activity_line=data.get("activity", "") or f"Email: {subj}")
        touched.add(addr)

    prov = f"{when[:10]} · {direction} · From: {from_addr} · Subject: {subj or '(no subject)'} · [{mid}]"
    if outbound and data.get("product_knowledge"):
        _append_kb(PRODUCT_KB, prov, data["product_knowledge"]); counters["prod"] += len(data["product_knowledge"])
    if data.get("operational_knowledge"):
        _append_kb(OPERATIONAL_KB, prov, data["operational_knowledge"]); counters["ops"] += len(data["operational_knowledge"])

    # Optional Q&A KB pass: for explanatory mail WE authored, harvest reusable customer-facing
    # Q&A pairs (semantic-deduped via embeddings). Only runs if qa_kb is present. Never breaks
    # the main pipeline.
    if outbound and from_addr in WRITERS and QA is not None:
        qp, qc = QA.harvest(subj, body_new, prov)
        counters["qa_prod"] = counters.get("qa_prod", 0) + qp
        counters["qa_comp"] = counters.get("qa_comp", 0) + qc
        if qp or qc:
            print(f"    qa-kb: +{qp} product, +{qc} company Q&A | {subj[:40]}")

    for fn, status in _ingest_attachments(rec.get("attachments", [])):
        counters["attach"] += 1
        print(f"    attachment: {fn} -> {status}")

    # extra pass: forwarded email — parse the ORIGINAL message embedded in the body.
    # When we forward a customer's email internally, the outer From: is us, so the
    # original sender + their inquiry would otherwise be lost. Extract them too.
    if is_forward(subj, body):
        _process_forward(rec, c, touched, counters, when, mid)

    # extra pass: if WE sent it (a known writer), collect writing-style observations
    if from_addr in WRITERS:
        obs = EX.extract_style(body_new)
        if obs:
            style_seen.setdefault(WRITERS[from_addr], []).extend(obs)
            print(f"    style: +{len(obs)} observations for {WRITERS[from_addr]}")

    DB.mark_processed(c, mid, when)
    c.commit()
    print(f"  [{rec.get('account','?')}] {direction} | {subj[:48]}")


def _update_contact_summary(c, addr):
    """Refresh one contact's email-grounded summary, folding in only new messages.

    Header-matched emails drive the summary; contacts seen only inside forwarded/
    quoted bodies (no header match) fall back to the activity-log summary.
    """
    row = DB.get_contact(c, addr)
    if not row:
        return
    emails = DB.emails_for_contact(c, addr)
    if emails:
        done = DB.summarized_ids(c, addr)
        new = [e for e in emails if e["id"] not in done]
        if not new:
            return
        # Rolling checkpoint: base_summary condenses everything up to the last sealed chunk;
        # we re-summarize only base_summary + the raw TAIL (msgs after the boundary), so old
        # chunks are never re-read. When the tail grows past the seal budget we fold it into
        # base_summary and advance the boundary — bounding compute to O(tail), not O(thread).
        base_summary, base_ids = DB.get_base(c, addr)
        tail = [e for e in emails if e["id"] not in base_ids]
        s = EX.summarize_contact_emails(row["name"], row["company"], base_summary, tail)
        if s:
            all_ids = [e["id"] for e in emails]
            DB.set_summary(c, addr, s, all_ids)
            if EX.should_seal(tail):                 # tail big enough — seal it into base
                DB.seal_base(c, addr, s, all_ids)
    elif (row["activity_log"] or "").strip():
        s = EX.summarize_activity(row["name"], row["company"], row["activity_log"])
        if s:
            DB.set_summary(c, addr, s)


def process():
    # EKB_MODE (set by run.sh): "night" = catch up the whole backlog aggressively;
    # "day" = only recent mail, gently. Defaults to day.
    mode = os.environ.get("EKB_MODE", "day")
    if mode == "night":
        cap, recent_only, newest_first = NIGHT_MAX, False, False
        # HARD STOP before 5:00 AM: cap the budget to (time until 5 AM − safety margin)
        # so old-email processing never bleeds into the morning.
        now = datetime.datetime.now()
        five = now.replace(hour=5, minute=0, second=0, microsecond=0)
        if now >= five:
            five += datetime.timedelta(days=1)
        secs_to_5 = (five - now).total_seconds() - 300   # 5-min margin (one in-flight email)
        budget = max(0, min(NIGHT_BUDGET, secs_to_5))
    else:
        cap, budget, recent_only, newest_first = DAY_MAX, DAY_BUDGET, True, True

    c = DB.conn()
    rows = DB.all_emails(c)
    if not rows:
        print(f"{_now()} no emails in archive yet — populate the emails table first.")
        return
    unproc = [r for r in rows if not DB.is_processed(c, r["id"])]
    if recent_only:
        cutoff = (datetime.datetime.now() - datetime.timedelta(days=RECENT_DAYS)).timestamp() * 1000
        # A thread with ANY recent message is "active" — process its whole backlog now
        # (e.g. older messages just backfilled for conversation completeness), not only
        # the messages that are themselves recent. Keeps full threads coherent in day mode.
        active_threads = {r.get("threadId") for r in rows
                          if r.get("threadId") and r.get("internalDate", 0) >= cutoff}
        unproc = [r for r in unproc
                  if r.get("internalDate", 0) >= cutoff or r.get("threadId") in active_threads]
    unproc.sort(key=lambda r: r.get("internalDate", 0), reverse=newest_first)
    todo = unproc[:cap]
    if not todo:
        print(f"{_now()} [{mode}] nothing to process ({len(rows)} corpus, "
              f"{sum(1 for r in rows if not DB.is_processed(c, r['id']))} unprocessed).")
        DB.export_csv(c); c.commit(); return

    touched, counters, style_seen = set(), {"prod": 0, "ops": 0, "attach": 0, "fwd": 0, "qa_prod": 0, "qa_comp": 0}, {}
    deadline = time.time() + budget
    print(f"{_now()} [{mode}] processing up to {len(todo)} (budget {budget}s) of {len(rows)} corpus emails...")
    done = 0
    for rec in todo:
        if time.time() > deadline:
            print(f"{_now()} [{mode}] time budget reached after {done}; stopping.")
            break
        _process_record(rec, c, touched, counters, style_seen)
        done += 1

    for addr in touched:
        _update_contact_summary(c, addr)
    c.commit()

    # merge this run's style observations into the bounded learned profiles
    for person, observations in style_seen.items():
        _refresh_style_profile(person, observations)

    n_contacts = DB.export_csv(c); c.commit()
    remaining = sum(1 for r in rows if not DB.is_processed(c, r["id"]))
    print(f"{_now()} [{mode}] run done: {done} emails | {len(touched)} contacts touched | "
          f"{counters['prod']} product + {counters['ops']} operational facts | "
          f"{counters.get('qa_prod',0)}+{counters.get('qa_comp',0)} Q&A (prod+comp) | "
          f"{counters['fwd']} forwards parsed | {counters['attach']} attachments | "
          f"{n_contacts} contacts total | {remaining} unprocessed left in corpus.")
    print(f"{_now()} [{mode}] reconciliation: {counters.get('miss',0)} contacts the LLM "
          f"missed (caught by deterministic pass) | {counters.get('halluc',0)} hallucinated "
          f"addresses dropped.")


if __name__ == "__main__":
    process()
